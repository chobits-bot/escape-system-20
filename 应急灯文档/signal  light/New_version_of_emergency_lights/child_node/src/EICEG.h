uint16_t colorWheel(uint8_t pos)
{
  if (pos < 85)
  {
    return dma_display->color565(pos * 3, 255 - pos * 3, 0);
  }
  else if (pos < 170)
  {
    pos -= 85;
    return dma_display->color565(255 - pos * 3, 0, pos * 3);
  }
  else
  {
    pos -= 170;
    return dma_display->color565(0, pos * 3, 255 - pos * 3);
  }
}
void init_EICEG()
{
  HUB75_I2S_CFG mxconfig(
      PANEL_RES_X,
      PANEL_RES_Y,
      PANEL_CHAIN);
  mxconfig.clkphase = false;
  mxconfig.driver = HUB75_I2S_CFG::FM6126A; // 选芯片型号{SHIFTREG=0, FM6124, FM6126A, FM6047, ICN2038S, MBI5124, SM5266P};
  dma_display = new MatrixPanel_I2S_DMA(mxconfig);
  dma_display->begin(25, 12, 26, 27, 13, 14, 21, 19, 5, 17, 18, 4, 2, 15);
  dma_display->setBrightness8(255); // 0-255-亮度
  dma_display->clearScreen();
}

void shift_pixels_right(unsigned char right[]) // 像素点向右移动
{
  for (int i = 0; i < 32; i++)
  {
    unsigned char data[4] = {right[(i * 4)], right[(i * 4) + 1], right[(i * 4) + 2], right[(i * 4) + 3]};
    if (data[3] & 0x01)
    {
      right[(i * 4)] = ((data[0] >> 1) | 0x80); // 32像素第4的第一位
    }
    else
    {
      right[(i * 4)] = (data[0] >> 1) | 0x00; // 32像素第4的第一位
    }
    if (data[2] & 0x01)
    {
      right[(i * 4) + 3] = ((data[3] >> 1) | 0x80); // 32像素第3的第一位--1
    }
    else
    {
      right[(i * 4) + 3] = (data[3] >> 1) | 0x00;
    }
    if (data[1] & 0x01)
    {
      right[(i * 4) + 2] = ((data[2] >> 1) | 0x80); // 32像素第2的第一位
    }
    else
    {
      right[(i * 4) + 2] = (data[2] >> 1) | 0x00;
    }
    if (data[0] & 0x01)
    {
      right[(i * 4) + 1] = ((data[1] >> 1) | 0x80); // 32像素第1的第一位
    }
    else
    {
      right[(i * 4) + 1] = (data[1] >> 1) | 0x00;
    }
  }
}
void shift_pixels_left(unsigned char left[]) // 像素点向左移动
{
  for (int i = 0; i < 32; i++)
  {
    unsigned char data[4] = {left[(i * 4)], left[(i * 4) + 1], left[(i * 4) + 2], left[(i * 4) + 3]};
    if (data[3] & 0x80)
    {
      left[(i * 4) + 2] = ((data[2] << 1) | 0x01); // 32像素第4的第一位
    }
    else
    {
      left[(i * 4) + 2] = (data[2] << 1) | 0x00; // 32像素第4的第一位
    }
    if (data[2] & 0x80)
    {
      left[(i * 4) + 1] = ((data[1] << 1) | 0x01); // 32像素第3的第一位--1
    }
    else
    {
      left[(i * 4) + 1] = (data[1] << 1) | 0x00;
    }
    if (data[1] & 0x80)
    {
      left[(i * 4)] = ((data[0] << 1) | 0x01); // 32像素第2的第一位
    }
    else
    {
      left[(i * 4)] = (data[0] << 1) | 0x00;
    }
    if (data[0] & 0x80)
    {
      left[(i * 4) + 3] = ((data[3] << 1) | 0x01); // 32像素第1的第一位
    }
    else
    {
      left[(i * 4) + 3] = (data[3] << 1) | 0x00;
    }
  }
}
void shift_pixels_up(unsigned char pixels[]) // 像素点向上移动
{
  unsigned char top_row_data[4] = {pixels[0], pixels[1], pixels[2], pixels[3]};
  for (int i = 0; i < (32 * 4) - 4; i++)
  {
    pixels[i] = pixels[i + 4];
  }
  pixels[(32 * 4) - 4] = top_row_data[0];
  pixels[(32 * 4) - 3] = top_row_data[1];
  pixels[(32 * 4) - 2] = top_row_data[2];
  pixels[(32 * 4) - 1] = top_row_data[3];
}
void shift_pixels_down(unsigned char pixels[])
{
  // 记录数组末尾的4个像素数据
  unsigned char bottom_row_data[4] = {pixels[(32 * 4) - 4], pixels[(32 * 4) - 3], pixels[(32 * 4) - 2], pixels[(32 * 4) - 1]};

  // 从数组末尾开始向下遍历，把每个字节都向下移动4个位置
  for (int i = (32 * 4) - 5; i >= 0; i--)
  {
    pixels[i + 4] = pixels[i];
  }

  // 将之前保存的末尾4个字节赋值到数组的开头
  pixels[0] = bottom_row_data[0];
  pixels[1] = bottom_row_data[1];
  pixels[2] = bottom_row_data[2];
  pixels[3] = bottom_row_data[3];
}

