void load(char status)
{
  long data = 0x00000000L;
  for (int i = 1; i <= status; i++)
  {
    data |= 0xC0000000;
    data >>= 3;
    data &= ~0xE0000000;
  }
  img_init_cloud[108] = img_init_cloud[104] = data >> 24;
  img_init_cloud[109] = img_init_cloud[105] = (data >> 16) & 0x00FF;
  img_init_cloud[110] = img_init_cloud[106] = (data >> 8) & 0x0000FF;
  img_init_cloud[111] = img_init_cloud[107] = (data & 0x000000FF);
  dma_display->drawBitmap(32, 0, img_init_cloud, 32, 32, myGREEN, myBLACK);
}
